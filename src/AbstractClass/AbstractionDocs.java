package AbstractClass;

public class AbstractionDocs {
	
	
/*
 * ========Abstraction=============================================
 * #process of hiding implementation details in the program is known as abstraction
 * #to achieve abstraction :
 * 		1> Abstraction class:
 * 
 * 		#class which contains abstract(un-implemented) and non-abstract (implemented)methods is known as abstract.
 *		
 *		#Abstract Method
 * 				->method which does not have implementation/body.
 * 				eg:
 * 		abstract void add();
 * 		abstract void print (String msg);
 * 
 * 		// non-abstract method
 * 			void addUser();
 *  
 * # we cannot create object/instance of abstract class.
 * # every abstract class must have at least one child class and child class must override abstract methods.
 * # level of abstraction 0-100%
 * 
 * 	Syntax:
 * 			abstract 	class class_name{
 * 		
 * 		//abstract method
 * 
 *		 e.g 
 *			abstract void print ();
 * 		//non-abstract methods
 * 		e.g
 * 			void sum(){
 * 			}
 * 
 * }
 * 
 * 2> Interface :
 * -> same as Class but which contains public abstract methods and public static final variables.
 * -> we cannot create object of interface
 * # every interface must have at least one implementation class and IMPL 
 * Class must override abstract methods.
 * 	-> 100% abstraction 
 * 		Syntax:
 * 		Interface Interface name {
 * 
 * 		//public static final variables
 * 		//public abstract method
 * 		}
 * 
 * */
}
